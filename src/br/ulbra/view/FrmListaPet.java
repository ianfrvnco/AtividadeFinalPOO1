    public void readJTable() throws SQLException {
        DefaultTableModel modelo = (DefaultTableModel) tabelaPet.getModel();
        modelo.setNumRows(0);
        PetDAO pdao = new PetDAO();

        for (Pet p: pdao.read()) {
            modelo.addRow(new Object[]{
                
                p.getId(),
                p.getNomePet(),
                p.getRaca(),
                p.getAnoNasc(),
                p.getCorPelo(),
                p.getSexo()

            });
        }
    }

