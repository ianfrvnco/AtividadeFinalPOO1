    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        PetDAO dao = null;
        try {
            Pet p = new Pet();
            dao = new PetDAO();

            p.setNomePet(txtNomePet.getText());
            p.setRaca(txtRaca.getText());
            p.setCorPelo(txtCorPelo.getText());
            
            p.setAnoNasc(Integer.parseInt(txtAnoNasc.getText()));

            if(btnRadMacho.isSelected()){
                
                p.setSexo("M");
            }else{
                p.setSexo("F");
            }
            
            if (p.validarNomePet(txtNomePet.getText())){
                dao.signUp(p);
            }
            
            this.dispose();
        } catch (SQLException ex) {
            Logger.getLogger(FrmCadUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

